def findLHS(nums):
    sorted_nums = sorted(nums)
    length= len(nums)
    output_dict = {0:0}
    end = length
    while end >=1:
        start = 0
        while start <= length-1:
            tem_arr = sorted_nums[start:end]
            if len(tem_arr)<=1:
                break
            if max(tem_arr) - min(tem_arr) == 1:
                output_dict[len(tem_arr)] = 0
                start += 1
            else:
                start +=1
        end -=1
    out = list(output_dict.keys())
    return max(out)


